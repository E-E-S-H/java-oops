class Method_demo
{
	public static int findsi(int p,int t,int r)//3
	{
		int output;//4
		output=p*t*r/100;//5
		return output;//6
	}
	public static void main(String[] args) 
	{
	int si;//1
	si=findsi(2000,2,3);//2&7
	System.out.println(si);	//8
	}
}                                                            