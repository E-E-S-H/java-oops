class Pcasting
{
	public static void main(String[] args) {
		int a=10;
		float f=a;
		System.out.println(f);

		float f1=10.09f;
		int a1=(int)f1;
		System.out.println(a1);

		long l=12345l;
		double d=l;
		System.out.println(d);

		double d1=12345.67;
		long l1=(long) d1;
		System.out.println(l1);

		char c='a';
		int x=(int)c;
		System.out.println(x);
		 float f2=124.45f;
		 byte b=(byte)f2;
		 System.out.println(b);
	}
}